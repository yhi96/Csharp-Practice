namespace _07.VowelOrConsonant
{
    internal class Program
    {
        static void Main(string[] args)
        {
            string letter = Console.ReadLine();

            if (letter == "A")
                Console.WriteLine("Vowel");
            else if (letter == "a")
                Console.WriteLine("Vowel");
            else if (letter == "E")
                Console.WriteLine("Vowel");
            else if (letter == "e")
                Console.WriteLine("Vowel");
            else if (letter == "I")
                Console.WriteLine("Vowel");
            else if (letter == "i")
                Console.WriteLine("Vowel");
            else if (letter == "O")
                Console.WriteLine("Vowel");
            else if (letter == "o")
                Console.WriteLine("Vowel");
            else if (letter == "U")
                Console.WriteLine("Vowel");
            else if (letter == "u")
                Console.WriteLine("Vowel");
            else
                Console.WriteLine("Consonant");
        }
    }
}